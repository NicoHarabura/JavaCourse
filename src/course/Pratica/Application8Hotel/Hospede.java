package course.Pratica.Application8Hotel;

public class Hospede {

	String nome;
	int quarto;

	public Hospede(String nome, int quarto) {
		this.nome = nome;
		this.quarto = quarto;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getQuarto() {
		return quarto;
	}
	
	

	
}
